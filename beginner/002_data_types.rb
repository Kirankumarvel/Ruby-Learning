# Basic Data Types in Ruby

# 1. Numbers
integer = 42
float = 3.14

# 2. Strings
string = "Hello, Ruby!"
single_quoted = 'Single quotes'

# 3. Booleans
true_value = true
false_value = false

# 4. Nil
nothing = nil

# 5. Symbols
symbol = :ruby_symbol

# 6. Arrays
array = [1, "two", 3.0, :four]

# 7. Hashes
hash = { key1: "value1", key2: "value2" }

puts "Integer: #{integer}"
puts "Float: #{float}"
puts "String: #{string}"
puts "Boolean true: #{true_value}"
puts "Boolean false: #{false_value}"
puts "Nil: #{nothing.inspect}"
puts "Symbol: #{symbol}"
puts "Array: #{array}"
puts "Hash: #{hash}"
